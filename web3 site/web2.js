const url = "https://eth-rinkeby.alchemyapi.io/v2";

const myAddress = "0xf4030DdD79fc7Fd49b25C976C5021D07568B4F91";
const provider = new ethers.providers.JsonRpcProvider(`${url}`);
$(document).ready(() => {
  const setAddress = (address) => {
    return address;
  };
  const signer = provider.getSigner(setAddress(myAddress));

  const contract = new ethers.Contract(contractAddress, contractAbi, provider);
  const signContract = new ethers.Contract(
    contractAddress,
    contractAbi,
    signer
  );
  console.log(signContract);
  const totalTokenStaked = async () => {
    let total = await signContract.totalStakes();
    return total;
  };
  //staking
  const createStake = async (amount) => {
    await signContract.createStake(amount);
  };
  //balance
  const balance = async (address) => {
    let bal = await signContract.balanceOf(address);
    return bal;
  };

  //transfer
  const transfer = async (reciever, amount) => {
    await signContract.transfer(reciever, amount);
  };

  //getting the users address

  $("#user--btn").click(() => {
    let userAddress = $("#wallet--address").val();
    console.log(userAddress);

    //view stacked token
    totalTokenStaked()
      .then((res) => {
        console.log(res);
        $("#token--stacked").text(`The total number of tokens stacked: ${res}`);
      })
      .catch((e) => {
        console.log(e);
      });

    //token to stack
    $(".stack--btn").click(() => {
      let stakeAmount = $("#stack--amount").val();
      createStake(Number(stakeAmount))
        .then((res) => {
          console.log(res);
          console.log("staked");
        })
        .catch((e) => console.log(e));
    });

    //token balance
    $(".balance").click(() => {
      balance(myAddress)
        .then((res) => {
          console.log(res);
          $(".view").text(`${res}`);
        })
        .catch((e) => {
          console.log(e);
        });
    });

    //transfer token
    $(".transfer").click(() => {
      let transferAmount = $("#transfer--amount").val();
      let transferAdress = $("#transfer--address").val();
      alert(`You are about to transfer ${transferAmount} to ${transferAdress}`);
      transfer(transferAdress, transferAmount)
        .then((res) => console.log(res))
        .catch((e) => console.log(e));
    });
  });
});
